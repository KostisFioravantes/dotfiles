#!/bin/zsh

ipv4="((\b25[0-5]|\b2[0-4]\d|\b[01]?\d\d?)(\.(25[0-5]|2[0-4]\d|[01]?\d\d?)){3})"
ipv6="(([^W_]{1,4}:){7,7}[^W_]{1,4}|([^W_]{1,4}:){1,7}:|([^W_]{1,4}:){1,6}:[^W_]{1,4}|([^W_]{1,4}:){1,5}(:[^W_]{1,4}){1,2}|([^W_]{1,4}:){1,4}(:[^W_]{1,4}){1,3}|([^W_]{1,4}:){1,3}(:[^W_]{1,4}){1,4}|([^W_]{1,4}:){1,2}(:[^W_]{1,4}){1,5}|[^W_]{1,4}:((:[^W_]{1,4}){1,6})|:((:[^W_]{1,4}){1,7}|:)|fe80:(:[^W_]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(ffff(:0{1,4}){0,1}:){0,1}((25[0-5]|(2[0-4]|1{0,1}\d){0,1}\d)\.){3,3}(25[0-5]|(2[0-4]|1{0,1}\d){0,1}\d)|([^W_]{1,4}:){1,4}:((25[0-5]|(2[0-4]|1{0,1}\d){0,1}\d)\.){3,3}(25[0-5]|(2[0-4]|1{0,1}\d){0,1}\d))"
port="((6553[0-5])|(655[0-2]\d)|(65[0-4]\d{2})|(6[0-4]\d{3})|([1-5]\d{4})|([0-5]{0,5})|(\d{1,4}))"
urlpath="(\/\S+)*(\/)?"
httpurl="http[s]?:\/\/($ipv4|$domain_name)(:$port_num)?$url_path"
ftp_url="ftp://([a-z0-9]+:[a-z0-9]+@)?([\\.a-z0-9]+)/([\\./a-z0-9]+)"

html_tags="<\/?[\w\s]*>|<.+[\W]>"
domain_name="((?:[a-z0-9](?:[a-z0-9-]{0,61}[a-z0-9])?\.)+[a-z0-9][a-z0-9-]{0,61}[a-z0-9])"
MAC="([0-9A-Fa-f]{2}[:-]){5}([0-9A-Fa-f]{2})|([^W_]{4}\\.[^W_]{4}\\.[^W_]{4})"

export	html_tags image_name email ipv4\
	ipv6 reL_domain_name MAC phone_num\
	port_num ftp_url http_url

hostnumber="()"
domainlabel="()"
hostname="(($domainlabel\.)*$toplabel)"
host="($hostname|$ipv4|\[$ipv6\])"
hostport="($host(:$port)?)"
login="(($user(:$password)?@)?$hostport)"
ip_schemepart="(\/\/$login(\/$urlpath)?)"
schemepart="($xchar*|$ip_schemepart)"
